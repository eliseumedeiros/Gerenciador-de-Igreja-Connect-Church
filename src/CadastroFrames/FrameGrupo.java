    private void carregarInfoGrupo(){
        lbLider.setText(grupo.getLider().getPessoa().getNome());
        lbTipoGrupo.setText(grupo.getTipoGrupo());
        lbDiaSemana.setText(grupo.getId().getDiaSemana());
        lbHora.setText(FormatoDataHora.userHora(grupo.getId().getHora()));
    }

    public final void atualizarTabela(){
        DefaultTableModel model = (DefaultTableModel) tabelaParticipantes.getModel();
        model.setRowCount(0);
        Object[] obj = new Object[4];
        for (Membro membro: membros){
            obj[0] = membro.getPessoa().getNome();
            obj[1] = membro.getPessoa().getSobrenome();
            obj[2] = membro.getPessoa().getEndBairro();
            obj[3] = membro.getPessoa().getEndCidade();
            model.addRow(obj);
        }
    }
       
