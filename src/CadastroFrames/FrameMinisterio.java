    public final void atualizarTabela(){
        DefaultTableModel model = (DefaultTableModel) tabelaParticipantes.getModel();
        model.setRowCount(0);
        Object[] objs = new Object[4];
        for (Membro participante: participantes){
            objs[0] = participante.getPessoa().getNome();
            objs[1] = participante.getPessoa().getSobrenome();
            objs[2] = participante.getPessoa().getEndBairro();
            objs[3] = participante.getPessoa().getEndCidade();
            model.addRow(objs);
        }
    }
    
    @Override
    public void tratarMembroSelecionado(Membro membro) {
        try {
            ministerio.adicionarMembro(membro);
            participantes = ministerio.selectMembrosParticipantes();
            atualizarTabela();
        } catch (Exception ex) {
            //Logger.getLogger(FrameMinisterio.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null,"Houve um problema ao adicionar membro: "+ex.toString());
        }
    }
    
