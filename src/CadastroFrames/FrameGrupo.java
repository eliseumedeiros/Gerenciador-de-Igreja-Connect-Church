    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int index = tabelaParticipantes.getSelectedRow();
        if (index == -1){
            JOptionPane.showMessageDialog(null, "Selecione um participante para excluir");
            return;
        }
        Membro excluido = membros.get(index);
        try {
            grupo.removerMembro(excluido);
            membros.remove(index);
            atualizarTabela();
        } catch (Exception ex) {
            //Logger.getLogger(FrameGrupo.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Houve um problema ao excluir membro: "+ex.toString());
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

