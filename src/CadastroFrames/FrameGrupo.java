     @Override
    public void tratarMembroSelecionado(Membro membro) {
        try {
            grupo.adicionarMembro(membro);
            //método não mto eficiente.. mas que funciona
            membros = grupo.selectMembros();
            atualizarTabela();
        } catch (Exception ex) {
            //Logger.getLogger(FrameGrupo.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Houve um erro ao adicionar o membro: "+ex.toString());
        }
    }

