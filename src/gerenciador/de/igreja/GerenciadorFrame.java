    private void btnExcluirGrupoGrupoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirGrupoGrupoActionPerformed
        int index = tabelaGrupos.getSelectedRow();
        if (index == -1){
            JOptionPane.showMessageDialog(null, "Selecione um grupo para excluir");
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(null,"Tem certeza de que deseja excluir o grupo?","", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION){
            return;
        }
        try{
            Grupo.deletarGrupo(igreja.getGrupos().get(index));
            refresh();
            JOptionPane.showMessageDialog(null, "Grupo excluído");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_btnExcluirGrupoGrupoActionPerformed

